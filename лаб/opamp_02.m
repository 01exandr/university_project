clear; % Очищення пам'ямті
clc; % Очищення екрану
C1=1e-6;
C2=10e-12;
R1=10e3;
R2=100e3;
T1=R1*C1;
T2=R2*C2;
T3=R2*C1;
NUM=[-T3 0];
DEN= [T1*T2 T1+T2 1];
W=tf(NUM, DEN); % Завдання передавальної функції
figure(1); %Ініціалізація вікна графіка
step(W); %Побудова перехідної функції
title('Перехідна функція');
grid; %Побудова сітки
figure(2);
bode(W);%Побудова ЛАЧХ та ЛФЧХ
grid;
title('Логарифмічні частотні характеристики');